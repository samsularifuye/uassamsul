	<b>Pakde Karwo: UMKM Jadi Pilihan Utama Pembangunan Ekonomi Jatim</b>
	Usaha Mikro, Kecil dan Menengah (UMKM) menjadi pilihan utama pembangunan ekonomi Jawa Timur.
		Sebab, UMKM mampu berkontribusi sebesar 54,98 persen terhadap PDRB Jatim Tahun 2015 Rp 1.689,88 
		triliun	, serta menyerap tenaga kerja sebanyak 11,12 juta orang.
	Hal tersebut disampaikan Gubernur Jatim Dr. H. Soekarwo saat menjadi keynote speaker Kuliah Umum 
	Universitas Airlangga Banyuwangi bertema “Arah Pembangunan Jawa Timur dengan Penguatan Daya Saing 
	dalam Perekonomian Global” di Aula SMKN 1 Banyuwangi, Kamis (15/12/2016) pagi.
Ia mengatakan, sektor UMKM ini telah terbukti mampu bertahan terhadap resesi dan krisis tahun 1998.
 Seiring berjalannya waktu, UMKM makin tumbuh dan menjadi kekuatan ekonomi yang berkontribusi besar 
 bagi bangsa ini. Jumlah UMKM mencapai 6,8 juta di tahun 2015 meningkat pesat dibanding tahun 2008 yang
 mencapai 4,2 juta UMKM.

Lebih lanjut disampaikannya, ada sekitar 70-80 persen UMKM yang hidupnya berasal dari biaya sendiri dan 
tidak ada bantuan dari pembiayaan atau industri jasa keuangan. Kondisi saat ini bunga kredit perbankan 
kepada UMKM sebesar 18-22 persen karena pelaku UMKM tidak punya agunan. Sedangkan bunga pinjaman untuk 
perusahaan besar (corporate) mencapai 12-14 persen.


