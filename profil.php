		<legend><b>SMK Negeri 1 Glagah</b> </legend>
	
	<br><br>
		SMK Negeri 1 Glagah adalah Sekolah menengah kejuruan yang terletak di Kelurahan Banjarsari, Kecamatan Glagah, Banyuwangi.<br>
		Sekolah ini dulunya bernama STM Banyuwangi dan mulai menerima siswa baru pada 1977, dan disahkan keberadaannya pada tahun 1980.<br>
		Sekolah ini populer dengan singkatan Semegah.
	<br><br>
	<b>Sejarah</b>
	<br><br>
	Pada awalnya SMK Negeri 1 Glagah (STM Banyuwangi) adalah peningkatan dari Sekolah Teknologi (ST) Negeri Banyuwangi pada tahun 1977, sesuai dengan SK Mendibud RI No. 0209/O/1980 tanggal 30 Juli 1980 dan memiliki 3 (tiga) jurusan yaitu : Mesin, Listrik dan Bangunan.

	Lokasi sekolah pada saat itu di Gedung milik Yayasan Blambangan, yang bertempat di Jalan Mawar No 16 Penataban, Giri (sekarang menjadi SMK PGRI 2 Banyuwangi (Kandangdoro)). Dalam perkembangan berikutnya pada tahun 1981 dimulailah proyek pembangunan STM Banyuwangi yang teletak di Jalan Kuntulan 1 Banjarsari, Glagah, Kabupaten Banyuwangi. Pada tahun 1985 diadakan relokasi ke gedung baru sekaligus mengembangkan diri menjadi 5 (lima) jurusan yakni Teknologi Pengolahan Logam, Bangunan, Listrik, Elektronika dan Otomotif.

	Sekolah ini berstatus Sekolah Standar Nasional setelah dihapuskannya status Rintisan Sekolah Berstandar Internasional (RSBI) pada tahun 2013. Pada 21 September 2007 SMK Negeri 1 Glagah Mendapatkan Sertifikat ISO 9001 : 2000 dari SAI Global dengan No Sertifikat QEC24485.
	<br><br>
